//! Explicit contract-case manifest for implemented MLS contracts.
//!
//! This table is the source of truth for mapping contract IDs to concrete tests.
//! It replaces heuristic discovery from test function names.

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum ContractCaseKind {
    Parse,
    Compile,
    Balance,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum ContractCaseOutcome {
    Accept,
    Reject,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct ContractCase {
    pub contract_id: &'static str,
    pub case_id: &'static str,
    pub test_file: &'static str,
    pub kind: ContractCaseKind,
    pub outcome: ContractCaseOutcome,
}

macro_rules! case {
    ($id:literal, $case:literal, $file:literal, $kind:ident, $outcome:ident) => {
        ContractCase {
            contract_id: $id,
            case_id: $case,
            test_file: $file,
            kind: ContractCaseKind::$kind,
            outcome: ContractCaseOutcome::$outcome,
        }
    };
}

